# This is a program to calculate total cost of popcorn at the movies.

# Create variables to store popcorn information
popcorn_cost = 4.25  # float variables
bags_purchased = 3  # int variable

# Multiply cost of popcorn by bags purchased to get total cost
total_cost = popcorn_cost * bags_purchased

print(total_cost)  # float as well because answer has decimal

