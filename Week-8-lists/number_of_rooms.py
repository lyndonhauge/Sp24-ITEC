"""
You can use len() to count the amount of elements in a list and this program does that,
then tells the user how many by storing the amount inside a variable and printing it.
"""

rooms = ['T.3010', 'T.3020', 'T.3030', 'T.3040', 'T.3050', 'T.3080',]

number_of_rooms = len(rooms)

print(f'There are {number_of_rooms} rooms in the ITEC department.')
