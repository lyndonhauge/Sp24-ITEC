"""
Introducing lists and how you can change the list.
"""


streets = ['Lake', 'Hennepin', 'Lyndale']
print(streets)  # prints ['Lake', 'Hennepin', 'Lyndale']
print(streets[1])  # prints 'Hennepin' (because it starts at 0)

streets[2] = 'Franklin'  # modifying element 2 to a new name

print(streets)  # prints ['Lake', 'Hennepin', 'Franklin']
